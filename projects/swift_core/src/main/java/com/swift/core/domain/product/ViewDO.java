package com.swift.core.domain.product;

public class ViewDO {

    private String element_types;
    private String element_titles;
    private String element_sources;
    private String element_parameters;

    public String getElement_types() {
        return element_types;
    }

    public void setElement_types(String element_types) {
        this.element_types = element_types;
    }

    public String getElement_titles() {
        return element_titles;
    }

    public void setElement_titles(String element_titles) {
        this.element_titles = element_titles;
    }

    public String getElement_sources() {
        return element_sources;
    }

    public void setElement_sources(String element_sources) {
        this.element_sources = element_sources;
    }

    public String getElement_parameters() {
        return element_parameters;
    }

    public void setElement_parameters(String element_parameters) {
        this.element_parameters = element_parameters;
    }
}
